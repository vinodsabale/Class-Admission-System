    public int getRNo() {
    int rno = 0;
    try {
        // Load the MySQL driver
        Class.forName("com.mysql.cj.jdbc.Driver");

        // Establish the database connection
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/csfsms?zeroDateTimeBehavior=CONVERT_TO_NULL", "root", "1111");

        // SQL query to get the max receipt number
        String sql = "SELECT MAX(rece_no) AS max_rece_no FROM fess_details";
        PreparedStatement st = con.prepareStatement(sql);
        ResultSet rs = st.executeQuery();

        // Get the result
        if (rs.next()) {
            rno = rs.getInt(1); // Fetch the value by index
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Please verify your database connection or table structure!", "Error", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    }
    return rno;
}

    
