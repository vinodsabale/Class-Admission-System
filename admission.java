    boolean validation()
    {
          if (txt_receiver_name.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter the Receiver Name first.");
            return false;
        }
        if (date.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Enter today's date.");
            return false;
        }
        if (txt_amount.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter the Amount (in numbers).");
            return false;
        }
        String paymentMode = combo_mode_payment.getSelectedItem().toString();
        if (paymentMode.equalsIgnoreCase("check")) {
            if (txt_check_no.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Enter the Check Number.");
                return false;
            }
            if (txt_bank_name.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Enter the Bank Name.");
                return false;
            }
        }
        if (paymentMode.equalsIgnoreCase("PhonePe")) {
            if (txt_upid.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Enter the UPID.");
                return false;
            }
        }
        return true;        
    }
    
    
    
